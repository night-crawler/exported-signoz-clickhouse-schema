SELECT
    state,
    ts,
    avg(per_series_value) AS value
FROM
(
    SELECT
        fingerprint,
        any(state) AS state,
        toStartOfInterval(toDateTime(intDiv(unix_milli, 1000)), toIntervalSecond(60)) AS ts,
        avg(value) AS per_series_value
    FROM signoz_metrics.distributed_samples_v4
    INNER JOIN
    (
        SELECT DISTINCT
            JSONExtractString(labels, 'state') AS state,
            fingerprint
        FROM signoz_metrics.time_series_v4
        WHERE (metric_name IN ['system_memory_usage']) AND (temporality = 'Cumulative') AND (__normalized = true) AND (unix_milli >= 1744743600000) AND (unix_milli < 1744748520000)
    ) AS filtered_time_series USING (fingerprint)
    WHERE (metric_name IN ['system_memory_usage']) AND (unix_milli >= 1744744920000) AND (unix_milli < 1744748520000)
    GROUP BY
        fingerprint,
        ts
    ORDER BY
        fingerprint ASC,
        ts ASC
)
WHERE isNaN(per_series_value) = 0
GROUP BY
    state,
    ts
ORDER BY
    state ASC,
    ts ASC

